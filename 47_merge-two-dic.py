
dict1 = {"a": 1, "b": 2}
dict2 = {"c": 3, "d": 4}

merged_dict = {**dict1, **dict2}

print("Merged dictionary:", merged_dict)






























# The ** operator is used to unpack the dictionary.
# It takes all the key-value pairs from the dictionaries and "unpacks"
# them into the new dictionary.